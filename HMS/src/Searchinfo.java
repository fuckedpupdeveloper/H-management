    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String SQL ="SELECT * FROM guest WHERE id=?";
      try
      {
        pst = conn.prepareStatement(SQL);
        pst.setInt(1,Integer.parseInt(jTextField1.getText()));
        rs = pst.executeQuery();
        if(rs.next())
        {
            String add1 = rs.getString("name");
            jTextField2.setText(add1);
            String add2 = rs.getString("address");
            jTextField4.setText(add2);
            String add3 = rs.getString("city");
            jTextField5.setText(add3);
            String add4 = rs.getString("roomtype");
            jTextField6.setText(add4);
           
            rs.close();
            pst.close();
        }
      }
      catch(Exception e)
      {
          JOptionPane.showMessageDialog(null, e);
      }
    }//GEN-LAST:event_jButton1ActionPerformed

